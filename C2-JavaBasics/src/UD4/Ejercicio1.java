package UD4;

public class Ejercicio1 {

	public static void main(String[] args) {
		float a=5, b=10, suma, resta, div, mult, mod;
		mod=a%b;
		resta=a-b;
		mult=a*b;
		suma=a+b;
		div=a/b;
	System.out.println("El resultado de 5+10 es " +suma+ ", de 5-10 es " +resta+", "
			+ "de 5*10 es "+mult+", de 5/10 es "+div+" y de 5%10 es "+mod);
	}

}
